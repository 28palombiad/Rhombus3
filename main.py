import turtle

screen = turtle.Screen()
screen.screensize(500,500)
screen.bgcolor("yellow")

t = turtle.Turtle()
t.fillcolor("cyan")
t.begin_fill()
t.penup()
t.setheading(45)
t.goto(-71,0)
t.pendown()
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.end_fill()
t.penup()
t.goto(0,0)

t.fillcolor("brown")
t.begin_fill()
t.penup()
t.setheading(45)
t.goto(71,0)
t.pendown()
t.forward(100)
